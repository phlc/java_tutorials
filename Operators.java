public class Operators{
  public static void main(String[] args){
    int i = 0;
    System.out.println(i);

    System.out.println(i++);

    System.out.println(i);

    System.out.println(++i);

    System.out.println(i);

    boolean bool = true;
    i = bool ? 11 : 44;
    System.out.println(i);

    bool = false;
    i = bool ? 11 : 44;
    System.out.println(i);

  }
}
